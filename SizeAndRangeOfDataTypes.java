import java.util.*;

public class SizeAndRangeOfDataTypes {
    public static void main(String[] args) {
      
System.out.println("Byte Size is: " + Byte.BYTES + " Range is from " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
System.out.println("Short Size is: " + Short.BYTES + " Range is from " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);
System.out.println("Integer Size is: " + Integer.BYTES + " Range is from " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
System.out.println("Long Size is: " + Long.BYTES + " Range is from " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);
System.out.println("Float Size is: " + Float.BYTES + " Range is from " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);
System.out.println("Double Size is: " + Double.BYTES + " Range is from " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);
System.out.println("Character Size is: " + Character.BYTES + " Range is from " + (int) Character.MIN_VALUE + " to " + (int) Character.MAX_VALUE);
System.out.println("Boolean Size is: " + "1" + " Possible values: " + Boolean.TRUE + " or " + Boolean.FALSE);
      
  }
}